    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed
        if (displayChoice == "Holiday")
        {
            results = bookingsClass.getHolidayStatus(chosenHolidayStatus);
        } else if (displayChoice == "Magician")
        {
            results = bookingsClass.getMagicianStatus(chosenMagicianStatus);
        } else if (displayChoice == "Waitlist")
        {
            results = bookingsClass.getWaitlistStatus();
        }

        Object[] resultsArray = new Object[results.size()];
        results.toArray(resultsArray);

        for (int i = 0; i < results.size(); i++)
        {
            jList2.setListData(resultsArray);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

