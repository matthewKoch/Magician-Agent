    private void ButtonHandlerBook(java.awt.event.ActionEvent evt)//GEN-FIRST:event_ButtonHandlerBook
    {//GEN-HEADEREND:event_ButtonHandlerBook
        customerName = jTextField1.getText();

        customers = customerClass.getCustomers(); 
        isNewCustomer = customerClass.getIsNewCustomer(customerName, customers);
        
        if (isNewCustomer == true)
        {
        customerClass.addCustomer(customerName);
        }

        isAvailableMagician = bookingsClass.getIsAvailableMagician(chosenHoliday);

        if (isAvailableMagician == true)
        {
            availableMagician = bookingsClass.getAvailableMagician(chosenHoliday);
            bookingsClass.addBookingEntry(chosenHoliday, customerName, availableMagician);
            jLabel8.setText(customerName + " has booked " + availableMagician + " for " + chosenHoliday + ".");
        } else
        {
            waitlistClass.addToWaitlist(customerName, chosenHoliday);
            jLabel8.setText(customerName + " has been added to the waitlist for " + chosenHoliday + ".");
        }

    }//GEN-LAST:event_ButtonHandlerBook

